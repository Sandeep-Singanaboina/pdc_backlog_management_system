    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         String hallticket=jTextField1.getText();
        String studentname = jTextField2.getText();
        String branch = jTextField3.getText();
        String groups = jTextField4.getText();
        String backlogs = jTextField5.getText();
        Connection con = null;
        PreparedStatement ps = null;
        try {
            Class.forName("org.sqlite.JDBC");
            con = DriverManager.getConnection("jdbc:sqlite:C:\\sqlite3\\pdc.db");
            con.setAutoCommit(false);
            System.out.println("Opened database successfully");
            String s = "INSERT INTO sem6(hallticket,studnetname,branch,groups,backlogs) VALUES(?,?,?,?,?)";
            ps = con.prepareStatement(s);
            ps.setString(1, hallticket);
            ps.setString(2,studentname);
            ps.setString(3,branch);
            ps.setString(4,groups);
            ps.setString(5,backlogs);
            int k = ps.executeUpdate();
            if (k != 0) {
                JOptionPane.showMessageDialog(null, "Saved Data of " + studentname + "");
            } else {
                JOptionPane.showMessageDialog(null, "Not saved Data of" + studentname + "");
            }
            ps.close();
            con.commit();
            con.close();
        } 
        catch (ClassNotFoundException | SQLException e) {
            System.err.println(e.getClass().getName() + ": " + e.getMessage());
            System.exit(0);
        }
        System.out.println("Records created successfully");
    }//GEN-LAST:event_jButton4ActionPerformed

